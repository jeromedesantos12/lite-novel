// export
module.exports = (mongoose) => {
  // db schema
  const User = mongoose.Schema({
    image: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
    },
  });

  return mongoose.model("Users", User);
};
